using System;

namespace CheckForChange
{
    class Program
    {
        static void Main()
        {
            bool isChanged = false; // Изменен ли программный код

            if (isChanged)
            {
                Console.WriteLine("Код изменен!");
            }
            else
            {
                Console.WriteLine("Код не изменен!");
            }
        }
    }
}
